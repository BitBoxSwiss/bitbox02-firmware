fn frame_index_for_logical_step(frame_idx: usize) -> usize {
    assert!(frame_idx < TOTAL_LOGICAL_STEPS);

    if frame_idx < LOCK_ANIMATION_FRAMES_STOP_TIME {
        return 0;
    }

    let mut actual_frame_idx = frame_idx - LOCK_ANIMATION_FRAMES_STOP_TIME;
    if actual_frame_idx < LOCK_ANIMATION_PAUSE_FRAME {
        return actual_frame_idx;
    }

    if actual_frame_idx < LOCK_ANIMATION_FRAMES_STOP_TIME + LOCK_ANIMATION_PAUSE_FRAME {
        return LOCK_ANIMATION_PAUSE_FRAME;
    }

    actual_frame_idx -= LOCK_ANIMATION_FRAMES_STOP_TIME;
    if actual_frame_idx >= LOCK_ANIMATION_ACTUAL_N_FRAMES {
        return LOCK_ANIMATION_ACTUAL_N_FRAMES - 1;
    }
    actual_frame_idx
}

fn frame_for_logical_step(frame_idx: usize) -> &'static [u8; FRAME_SIZE] {
    &LOCK_ANIMATION[frame_index_for_logical_step(frame_idx)]
}

fn step_deadline_ms(step: usize) -> u32 {
    assert!(step <= TOTAL_LOGICAL_STEPS);
    (u32::try_from(step).unwrap() * TOTAL_DURATION_MS) / u32::try_from(TOTAL_LOGICAL_STEPS).unwrap()
}

fn step_interval_ms(step: usize) -> u32 {
    assert!(step < TOTAL_LOGICAL_STEPS);
    step_deadline_ms(step + 1) - step_deadline_ms(step)
}

fn frame_bit_is_set(frame: &[u8; FRAME_SIZE], x: usize, y: usize) -> bool {
    let bit_index = y * FRAME_WIDTH + x;
    let byte = frame[bit_index / 8];
    let mask = 0x80 >> (bit_index % 8);
    byte & mask != 0
}

fn render_frame(canvas: &LvCanvas, logical_step: usize) {
    let frame = frame_for_logical_step(logical_step);
    for y in 0..FRAME_HEIGHT {
        for x in 0..FRAME_WIDTH {
            let (color, opa) = if frame_bit_is_set(frame, x, y) {
                (lvgl::color::white(), LvOpacityLevel::LV_OPA_COVER as u8)
            } else {
                (lvgl::color::black(), LvOpacityLevel::LV_OPA_TRANSP as u8)
            };
            canvas.set_px(
                i32::try_from(x).unwrap(),
                i32::try_from(y).unwrap(),
                color,
                opa,
            );
        }
    }
}

struct UnlockAnimationScreen {
    screen: LvObj,
    canvas: LvCanvas,
}

fn build_unlock_animation_screen() -> UnlockAnimationScreen {
    let screen = LvObj::new().unwrap();
    screen.set_style_bg_color(lvgl::color::black(), 0);
    screen.set_style_bg_opa(LvOpacityLevel::LV_OPA_COVER as u8, 0);

    let canvas = LvCanvas::new(
        &screen,
        vec![[0, 0, 0, 0]; FRAME_WIDTH * FRAME_HEIGHT],
        u32::try_from(FRAME_WIDTH).unwrap(),
        u32::try_from(FRAME_HEIGHT).unwrap(),
    )
    .unwrap();
    canvas.set_style_bg_opa(LvOpacityLevel::LV_OPA_TRANSP as u8, 0);
    // TODO: remove this temporary scaling and replace the imported BB02 source frames with final
    // renderings sized for the BB03 display.
    canvas.set_pivot(
        i32::try_from(FRAME_WIDTH / 2).unwrap(),
        i32::try_from(FRAME_HEIGHT / 2).unwrap(),
    );
    canvas.set_antialias(false);
    canvas.set_scale(DISPLAY_SCALE);
    canvas.set_size(
        canvas.get_transformed_width(),
        canvas.get_transformed_height(),
    );
    canvas.set_inner_align(lvgl::LvImageAlign::LV_IMAGE_ALIGN_CENTER);
    canvas.align(LvAlign::LV_ALIGN_CENTER, 0, 0);

    UnlockAnimationScreen { screen, canvas }
}

pub(super) async fn show<Timer: bitbox_hal::timer::Timer>(ui: &mut super::BitBox03Ui<Timer>) {
    let UnlockAnimationScreen { screen, canvas } = build_unlock_animation_screen();
    let _screen = ui.push_guard(screen);

    for logical_step in 0..TOTAL_LOGICAL_STEPS {
        render_frame(&canvas, logical_step);
        Timer::delay_for(Duration::from_millis(u64::from(step_interval_ms(
            logical_step,
        ))))
        .await;
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_frame_mapping() {
        assert_eq!(frame_index_for_logical_step(0), 0);
        assert_eq!(
            frame_index_for_logical_step(LOCK_ANIMATION_FRAMES_STOP_TIME - 1),
            0
        );
        assert_eq!(
            frame_index_for_logical_step(LOCK_ANIMATION_FRAMES_STOP_TIME),
            0
        );
        assert_eq!(
            frame_index_for_logical_step(LOCK_ANIMATION_FRAMES_STOP_TIME + 1),
            1
        );
        assert_eq!(
            frame_index_for_logical_step(
                LOCK_ANIMATION_FRAMES_STOP_TIME + LOCK_ANIMATION_PAUSE_FRAME
            ),
            2
        );
        assert_eq!(
            frame_index_for_logical_step(
                LOCK_ANIMATION_FRAMES_STOP_TIME
                    + LOCK_ANIMATION_PAUSE_FRAME
                    + LOCK_ANIMATION_FRAMES_STOP_TIME
                    - 1
            ),
            2
        );
        assert_eq!(
            frame_index_for_logical_step(
                LOCK_ANIMATION_FRAMES_STOP_TIME
                    + LOCK_ANIMATION_PAUSE_FRAME
                    + LOCK_ANIMATION_FRAMES_STOP_TIME
            ),
            2
        );
        assert_eq!(frame_index_for_logical_step(TOTAL_LOGICAL_STEPS - 1), 9);
    }

    #[test]
    fn test_duration_schedule() {
        let total = (0..TOTAL_LOGICAL_STEPS).map(step_interval_ms).sum::<u32>();
        assert_eq!(total, TOTAL_DURATION_MS);
    }

    #[test]
    fn test_duration_schedule_monotonic() {
        for step in 0..TOTAL_LOGICAL_STEPS {
            assert!(step_interval_ms(step) > 0);
            assert!(step_deadline_ms(step + 1) > step_deadline_ms(step));
        }
    }
}
